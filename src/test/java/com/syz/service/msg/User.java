package com.syz.service.msg;

public class User {
	/**
	 * 主键ID
	 */
	private Integer id;
	/**
	 * 姓名
	 */
	private String name;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
